x = [1, 2, 3, 4, 5]

y=x[0:2] # This will extract the data from 0 to 2 elements from the list.
print(y)

y=x[0:] # This will print all the elements from 0 to the end of the list.
print(y)

y=x[:10] # This will print the all elements from 0 to 10th element.
print(y)

y=x[:-1] # This will print the all the elements other than the last element.
print(y)

##Above process called slicing##

print(len(x)) # This will print how many elements inside the list.

