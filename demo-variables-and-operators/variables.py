var_string_01 = "test-value-01" # Define a variable
var_integer_01 = 10
var_integer_02 = 2

print(var_string_01*2) # Print the string variable
print(var_integer_01) # Print the integer variable

# Simple mathematic operations
print(var_integer_01 + var_integer_02) # Adding two numbers
print(var_integer_01 - var_integer_02) # Deduction of two numbers
print(var_integer_01 * var_integer_02) # Multiply two numbers
print(var_integer_01 / var_integer_02) # Devide two number

# Special operators
print(var_integer_01 % var_integer_02) # Modulus operator - get the reminder after deviding two numbers
print(var_integer_01 // var_integer_02) # Floor division operator - Get the floor value after deviding
print(var_integer_01 ** var_integer_02) # Exponentiation operator- get the square value [10 into the power 2]

